def train_model(data_dict, save_dir, hidden_size=64, dt=0.1):
    os.makedirs(save_dir, exist_ok=True)
    device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
    print(f"Device: {device}  hidden={hidden_size}  dt={dt}")
    print(f"Physics weights: lambda_P={LAMBDA_PHYS_P}  lambda_Q={LAMBDA_PHYS_Q}  "
          f"lambda_nonneg={LAMBDA_NONNEG}  lambda_bounds={LAMBDA_BOUNDS}")
    print(f"Tolerances: eps_P={EPSILON_P} W  eps_Q={EPSILON_Q} VAR")

    # ── Sequences ──────────────────────────────────────────────────────────
    X_tr, YP_tr, YQ_tr = create_sequences(data_dict['train'], WIN)
    X_va, YP_va, YQ_va = create_sequences(data_dict['val'],   WIN)
    X_te, YP_te, YQ_te = create_sequences(data_dict['test'],  WIN)

    # ── Scaling ────────────────────────────────────────────────────────────
    # Separate scalers for P_agg and Q_agg channels
    p_scaler = MinMaxScaler()
    q_scaler = MinMaxScaler()

    X_tr_p = p_scaler.fit_transform(X_tr[:, :, 0].reshape(-1, 1)).reshape(X_tr.shape[0], WIN)
    X_tr_q = q_scaler.fit_transform(X_tr[:, :, 1].reshape(-1, 1)).reshape(X_tr.shape[0], WIN)
    X_va_p = p_scaler.transform(X_va[:, :, 0].reshape(-1, 1)).reshape(X_va.shape[0], WIN)
    X_va_q = q_scaler.transform(X_va[:, :, 1].reshape(-1, 1)).reshape(X_va.shape[0], WIN)
    X_te_p = p_scaler.transform(X_te[:, :, 0].reshape(-1, 1)).reshape(X_te.shape[0], WIN)
    X_te_q = q_scaler.transform(X_te[:, :, 1].reshape(-1, 1)).reshape(X_te.shape[0], WIN)

    X_tr = np.stack([X_tr_p, X_tr_q], axis=2)  # (N, WIN, 2)
    X_va = np.stack([X_va_p, X_va_q], axis=2)
    X_te = np.stack([X_te_p, X_te_q], axis=2)

    # Per-appliance P and Q scalers
    yp_scalers, yq_scalers = [], []
    for i in range(len(APPLIANCES)):
        yps = MinMaxScaler(); yqs = MinMaxScaler()
        YP_tr[:, i:i+1] = yps.fit_transform(YP_tr[:, i:i+1])
        YP_va[:, i:i+1] = yps.transform(YP_va[:, i:i+1])
        YP_te[:, i:i+1] = yps.transform(YP_te[:, i:i+1])
        YQ_tr[:, i:i+1] = yqs.fit_transform(YQ_tr[:, i:i+1])
        YQ_va[:, i:i+1] = yqs.transform(YQ_va[:, i:i+1])
        YQ_te[:, i:i+1] = yqs.transform(YQ_te[:, i:i+1])
        yp_scalers.append(yps)
        yq_scalers.append(yqs)

    thresholds_scaled = [
        (THRESHOLDS[app] - float(yp_scalers[i].data_min_[0]))
        / float(yp_scalers[i].data_range_[0])
        for i, app in enumerate(APPLIANCES)
    ]

    print(f"Train: {X_tr.shape}  YP:{YP_tr.shape}  YQ:{YQ_tr.shape}")
    print(f"Val:   {X_va.shape}  YP:{YP_va.shape}  YQ:{YQ_va.shape}")
    print(f"Test:  {X_te.shape}  YP:{YP_te.shape}  YQ:{YQ_te.shape}")

    tr_loader = torch.utils.data.DataLoader(
        WHITEDDataset(X_tr, YP_tr, YQ_tr), batch_size=BATCH, shuffle=True,  drop_last=False)
    va_loader = torch.utils.data.DataLoader(
        WHITEDDataset(X_va, YP_va, YQ_va), batch_size=BATCH, shuffle=False, drop_last=False)
    te_loader = torch.utils.data.DataLoader(
        WHITEDDataset(X_te, YP_te, YQ_te), batch_size=BATCH, shuffle=False, drop_last=False)

    # ── Model + losses ─────────────────────────────────────────────────────
    model = ReactivePowerBasicLNNModel(
        input_size=2, hidden_size=hidden_size,
        n_appliances=len(APPLIANCES), dt=dt,
    ).to(device)

    mse_loss    = nn.MSELoss()
    phys_loss   = MultiPhysicsLoss(
        p_scaler, q_scaler, yp_scalers, yq_scalers,
        APPLIANCES, epsilon_p=EPSILON_P, epsilon_q=EPSILON_Q,
    ).to(device)

    optimizer = torch.optim.Adam(model.parameters(), lr=LR)
    scheduler = torch.optim.lr_scheduler.ReduceLROnPlateau(
        optimizer, mode='min', factor=0.5, patience=8, min_lr=1e-5)

    history = {
        'train_loss': [], 'val_loss': [],
        'train_mse_p': [], 'train_mse_q': [],
        'val_mse_p':   [], 'val_mse_q':   [],
        'val_metrics': [],
    }
    best_val_loss = float('inf')
    best_state    = None
    counter       = 0

    n_params = sum(p.numel() for p in model.parameters() if p.requires_grad)
    print(f"Parameters: {n_params:,}")
    print("Starting PINN-ReactivePower-BasicLNN training ...")

    for epoch in range(EPOCHS):
        # ── Train ──────────────────────────────────────────────────────────
        model.train()
        ep_total = ep_mse_p = ep_mse_q = 0.0
        pbar = tqdm(tr_loader, desc=f"Epoch {epoch+1}/{EPOCHS}", leave=False)

        for xb, yb_p, yb_q in pbar:
            xb, yb_p, yb_q = xb.to(device), yb_p.to(device), yb_q.to(device)
            optimizer.zero_grad()

            p_pred, q_pred = model(xb)

            l_mse_p = mse_loss(p_pred, yb_p)
            l_mse_q = mse_loss(q_pred, yb_q)

            p_mid = xb[:, WIN // 2, 0]   # scaled P_agg at midpoint
            q_mid = xb[:, WIN // 2, 1]   # scaled Q_agg at midpoint

            if epoch < WARMUP_EPOCHS:
                loss = l_mse_p + l_mse_q
            else:
                l_pp, l_pq, l_nn, l_bd = phys_loss(p_mid, q_mid, p_pred, q_pred)

                bce = torch.tensor(0.0, device=device)
                for i, app in enumerate(APPLIANCES):
                    if BCE_LAMBDA[app] > 0:
                        pi    = p_pred[:, i].clamp(1e-7, 1 - 1e-7)
                        y_bin = (yb_p[:, i] > thresholds_scaled[i]).float()
                        w     = torch.where(y_bin == 1,
                                            torch.full_like(y_bin, BCE_ALPHA[app]),
                                            torch.ones_like(y_bin))
                        bce = bce + BCE_LAMBDA[app] * F.binary_cross_entropy(pi, y_bin, weight=w)

                loss = (l_mse_p + l_mse_q
                        + LAMBDA_PHYS_P  * l_pp
                        + LAMBDA_PHYS_Q  * l_pq
                        + LAMBDA_NONNEG  * l_nn
                        + LAMBDA_BOUNDS  * l_bd
                        + bce)

            loss.backward()
            torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)
            optimizer.step()

            ep_total += loss.item()
            ep_mse_p += l_mse_p.item()
            ep_mse_q += l_mse_q.item()
            pbar.set_postfix({'mse_p': f'{l_mse_p.item():.5f}',
                              'mse_q': f'{l_mse_q.item():.5f}'})

        n_tr = len(tr_loader)
        history['train_loss'].append(ep_total / n_tr)
        history['train_mse_p'].append(ep_mse_p / n_tr)
        history['train_mse_q'].append(ep_mse_q / n_tr)

        # ── Validate ───────────────────────────────────────────────────────
        model.eval()
        vl_total = vl_mse_p = vl_mse_q = 0.0
        val_pp, val_pt = [], []

        with torch.no_grad():
            for xb, yb_p, yb_q in va_loader:
                xb, yb_p, yb_q = xb.to(device), yb_p.to(device), yb_q.to(device)
                p_pred, q_pred  = model(xb)
                l_mse_p = mse_loss(p_pred, yb_p)
                l_mse_q = mse_loss(q_pred, yb_q)
                vl_mse_p += l_mse_p.item()
                vl_mse_q += l_mse_q.item()
                vl_total += (l_mse_p + l_mse_q).item()
                val_pp.append(p_pred.cpu().numpy())
                val_pt.append(yb_p.cpu().numpy())

        n_va = len(va_loader)
        avg_va_total = vl_total / n_va
        history['val_loss'].append(avg_va_total)
        history['val_mse_p'].append(vl_mse_p / n_va)
        history['val_mse_q'].append(vl_mse_q / n_va)

        scheduler.step(vl_mse_p / n_va)

        yp_pred_all = np.concatenate(val_pp)
        yp_true_all = np.concatenate(val_pt)
        per_app     = compute_metrics(yp_true_all, yp_pred_all, yp_scalers)
        history['val_metrics'].append(per_app)

        avg_f1  = np.mean([per_app[a]['f1']  for a in APPLIANCES])
        avg_mae = np.mean([per_app[a]['mae'] for a in APPLIANCES])

        print(f"  Epoch {epoch+1:3d}/{EPOCHS}  "
              f"train={ep_total/n_tr:.5f}  val={avg_va_total:.5f}  "
              f"avgF1={avg_f1:.4f}  avgMAE={avg_mae:.2f}  "
              f"lr={optimizer.param_groups[0]['lr']:.2e}")
        for app in APPLIANCES:
            m = per_app[app]
            print(f"    {app:<16}  F1={m['f1']:.4f}  "
                  f"P={m['precision']:.4f}  R={m['recall']:.4f}  "
                  f"MAE={m['mae']:.2f}  SAE={m['sae']:.4f}")

        if avg_va_total < best_val_loss:
            best_val_loss = avg_va_total
            best_state    = {k: v.clone() for k, v in model.state_dict().items()}
            counter       = 0
        else:
            counter += 1
            if counter >= PATIENCE:
                print(f"  Early stopping at epoch {epoch+1}")
                break

    print("Training completed!")

    # ── Test ───────────────────────────────────────────────────────────────
    if best_state is not None:
        model.load_state_dict(best_state)
    model.eval()

    te_pp, te_pt = [], []
    with torch.no_grad():
        for xb, yb_p, _ in te_loader:
            p_pred, _ = model(xb.to(device))
            te_pp.append(p_pred.cpu().numpy())
            te_pt.append(yb_p.numpy())

    test_metrics = compute_metrics(np.concatenate(te_pt),
                                   np.concatenate(te_pp), yp_scalers)

    print(f"\n{'Appliance':<17} {'F1':>8} {'Precision':>10} {'Recall':>8} "
          f"{'MAE':>8} {'SAE':>8}")
    print("-" * 65)
    for app in APPLIANCES:
        m = test_metrics[app]
        print(f"{app:<17} {m['f1']:>8.4f} {m['precision']:>10.4f} "
              f"{m['recall']:>8.4f} {m['mae']:>8.2f} {m['sae']:>8.4f}")

    _plot(history, test_metrics, save_dir)

    config = {
        'dataset': 'WHITED',
        'model': 'ReactivePowerBasicLNNModel',
        'description': (
            'BasicLNN (fixed tau) on [P_agg, Q_agg] input + P/Q conservation '
            '+ non-negativity + bounds constraints'
        ),
        'physics': {
            'lambda_phys_p': LAMBDA_PHYS_P, 'epsilon_p_W': EPSILON_P,
            'lambda_phys_q': LAMBDA_PHYS_Q, 'epsilon_q_VAR': EPSILON_Q,
            'lambda_nonneg': LAMBDA_NONNEG,  'lambda_bounds': LAMBDA_BOUNDS,
        },
        'window_size': WIN,
        'model_params': {'input_size': 2, 'hidden_size': hidden_size,
                         'n_appliances': len(APPLIANCES), 'dt': dt},
        'train_params': {'lr': LR, 'epochs': EPOCHS, 'patience': PATIENCE,
                         'warmup_epochs': WARMUP_EPOCHS},
        'test_metrics': {app: {k: float(v) for k, v in m.items()}
                         for app, m in test_metrics.items()},
    }
    with open(os.path.join(save_dir, 'pinn_reactive_power_lnn_whited_results.json'),
              'w', encoding='utf-8') as f:
        json.dump(config, f, indent=4)

    return test_metrics, history


