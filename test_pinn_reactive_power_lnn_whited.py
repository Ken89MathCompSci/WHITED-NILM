class WHITEDDataset(torch.utils.data.Dataset):
    def __init__(self, X, Y_P, Y_Q):
        self.X   = torch.FloatTensor(X)
        self.Y_P = torch.FloatTensor(Y_P)
        self.Y_Q = torch.FloatTensor(Y_Q)

    def __len__(self):
        return len(self.X)

    def __getitem__(self, idx):
        return self.X[idx], self.Y_P[idx], self.Y_Q[idx]


