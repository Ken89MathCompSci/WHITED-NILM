# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

EPOCHS        = 80
PATIENCE      = 20
LR            = 1e-3
BATCH         = 32
WIN           = 100
STRIDE        = 5

WARMUP_EPOCHS = 20

# Physics loss weights
LAMBDA_PHYS_P  = 0.01   # active-power conservation
LAMBDA_PHYS_Q  = 0.01   # reactive-power conservation
LAMBDA_NONNEG  = 0.005  # non-negativity on P
LAMBDA_BOUNDS  = 0.005  # P upper-bound (scaled > 1.0)

EPSILON_P = 50.0   # tolerance on P sum violation (Watts)
EPSILON_Q = 30.0   # tolerance on Q sum violation (VAR)

APPLIANCES = ['fridge', 'microwave', 'washing machine', 'kettle']

THRESHOLDS = {
    'fridge':           50.0,
    'microwave':        50.0,
    'washing machine':   5.0,
    'kettle':           50.0,
}

BCE_LAMBDA = {'fridge': 0.3, 'microwave': 0.3, 'washing machine': 0.3, 'kettle': 0.3}
BCE_ALPHA  = {'fridge': 1.5, 'microwave': 1.5, 'washing machine': 4.0, 'kettle': 1.5}


# ---------------------------------------------------------------------------
# Multi-physics loss
# ---------------------------------------------------------------------------

class MultiPhysicsLoss(nn.Module):
    """
    Differentiable physics constraints using inverse MinMaxScaler transforms.

    Registered buffers store the scaler parameters so the loss module can
    move to GPU with .to(device).
    """

    def __init__(self, p_scaler, q_scaler, yp_scalers, yq_scalers,
                 appliances, epsilon_p=EPSILON_P, epsilon_q=EPSILON_Q):
        super().__init__()
        self.epsilon_p = epsilon_p
        self.epsilon_q = epsilon_q

        # Mains P inverse-scale params
        self.register_buffer('p_min',   torch.tensor(float(p_scaler.data_min_[0])))
        self.register_buffer('p_range', torch.tensor(float(p_scaler.data_range_[0])))

        # Mains Q inverse-scale params
        self.register_buffer('q_min',   torch.tensor(float(q_scaler.data_min_[0])))
        self.register_buffer('q_range', torch.tensor(float(q_scaler.data_range_[0])))

        # Per-appliance P inverse-scale params
        yp_mins   = [float(yp_scalers[i].data_min_[0])   for i in range(len(appliances))]
        yp_ranges = [float(yp_scalers[i].data_range_[0]) for i in range(len(appliances))]
        self.register_buffer('yp_mins',   torch.tensor(yp_mins,   dtype=torch.float32))
        self.register_buffer('yp_ranges', torch.tensor(yp_ranges, dtype=torch.float32))

        # Per-appliance Q inverse-scale params
        yq_mins   = [float(yq_scalers[i].data_min_[0])   for i in range(len(appliances))]
        yq_ranges = [float(yq_scalers[i].data_range_[0]) for i in range(len(appliances))]
        self.register_buffer('yq_mins',   torch.tensor(yq_mins,   dtype=torch.float32))
        self.register_buffer('yq_ranges', torch.tensor(yq_ranges, dtype=torch.float32))

    def forward(self, p_agg_sc, q_agg_sc, p_pred_sc, q_pred_sc):
        """
        Args:
            p_agg_sc:  (batch,)       scaled P_agg at window midpoint
            q_agg_sc:  (batch,)       scaled Q_agg at window midpoint
            p_pred_sc: (batch, n_app) scaled per-appliance P predictions
            q_pred_sc: (batch, n_app) scaled per-appliance Q predictions
        Returns:
            l_phys_p, l_phys_q, l_nonneg, l_bounds  — all scalar tensors
        """
        # Inverse-scale to raw Watts / VAR
        p_agg_raw  = p_agg_sc * self.p_range + self.p_min     # (batch,)
        q_agg_raw  = q_agg_sc * self.q_range + self.q_min     # (batch,)
        p_pred_raw = p_pred_sc * self.yp_ranges + self.yp_mins  # (batch, n_app)
        q_pred_raw = q_pred_sc * self.yq_ranges + self.yq_mins  # (batch, n_app)

        # 1. Active-power conservation (one-sided: background loads absorb slack)
        l_phys_p = F.relu(p_pred_raw.sum(dim=1) - p_agg_raw - self.epsilon_p).mean()

        # 2. Reactive-power conservation (symmetric: Q can go either sign)
        l_phys_q = (q_pred_raw.sum(dim=1) - q_agg_raw).abs().clamp(min=self.epsilon_q).mean()

        # 3. Non-negativity on predicted P (power consumption >= 0)
        l_nonneg = F.relu(-p_pred_raw).mean()

        # 4. Per-appliance P upper-bound (predictions > scaled maximum = 1.0)
        l_bounds = F.relu(p_pred_sc - 1.0).mean()

        return l_phys_p, l_phys_q, l_nonneg, l_bounds


# ---------------------------------------------------------------------------
# Model
# ---------------------------------------------------------------------------

class ReactivePowerBasicLNNModel(nn.Module):
    """
    BasicLiquidTimeLayer (fixed tau) on 2-channel input [P_agg, Q_agg].

    Per-appliance heads produce both P and Q predictions so the reactive-power
    conservation constraint is differentiable end-to-end.

    Cell update:
        tau   = softplus(tau_param)
        f_t   = tanh(LayerNorm(W_in * x_t + W_rec * h))
        dh    = (-h / tau + f_t) * dt
        h_new = clamp(h + dh, -10, 10)
    """

    def __init__(self, input_size=2, hidden_size=64, n_appliances=4, dt=0.1):
        super().__init__()
        self.hidden_size  = hidden_size
        self.n_appliances = n_appliances
        self.dt           = dt

        self.input_proj  = nn.Linear(input_size, hidden_size)
        self.tau         = nn.Parameter(torch.ones(hidden_size))
        self.rec_weights = nn.Parameter(torch.empty(hidden_size, hidden_size))
        nn.init.xavier_uniform_(self.rec_weights)

        self.intra_norm = nn.LayerNorm(hidden_size)
        self.norm       = nn.LayerNorm(hidden_size)

        self.p_heads = nn.ModuleList([nn.Linear(hidden_size, 1) for _ in range(n_appliances)])
        self.q_heads = nn.ModuleList([nn.Linear(hidden_size, 1) for _ in range(n_appliances)])

    def forward(self, x):
        """
        Args:
            x: (batch, seq_len, 2)  — [P_agg_scaled, Q_agg_scaled]
        Returns:
            p_out: (batch, n_appliances)
            q_out: (batch, n_appliances)
        """
        batch_size, seq_len, _ = x.size()
        h   = torch.zeros(batch_size, self.hidden_size, device=x.device)
        tau = F.softplus(self.tau).unsqueeze(0)

        for t in range(seq_len):
            x_t   = x[:, t, :]
            f_t   = torch.tanh(self.intra_norm(self.input_proj(x_t) + torch.matmul(h, self.rec_weights)))
            dh    = (-h / tau + f_t) * self.dt
            h     = (h + dh).clamp(-10.0, 10.0)

        h = self.norm(h)
        p_out = torch.cat([head(h) for head in self.p_heads], dim=1)
        q_out = torch.cat([head(h) for head in self.q_heads], dim=1)
        return p_out, q_out


