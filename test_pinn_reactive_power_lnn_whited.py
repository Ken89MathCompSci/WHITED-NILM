"""
Physics-Informed Basic LNN with Reactive Power for NILM — WHITED.

Extends the standard PINN-BasicLNN with three additional physics constraints:

  1. Active-power conservation (P)   — ReLU(sum P_hat_i - P_agg - eps_P)
     Already in test_pinn_basic_lnn_whited_specific_splits.py.

  2. Reactive-power conservation (Q) — |sum Q_hat_i - Q_agg|
     WHITED provides raw V+I waveforms, so Q is computed at the 50 Hz
     fundamental (Austrian grid) via DFT.  Q > 0 for inductive loads
     (fridge, washing machine), Q ~= 0 for resistive loads (kettle).
     Using both P and Q as input features helps discriminate appliance types
     that look similar in active power alone.

  3. Non-negativity on predicted P   — ReLU(-P_hat_raw)

  4. Per-appliance P upper-bound     — ReLU(P_hat_scaled - 1.0)
     MinMaxScaler maps the training maximum to 1.0; predictions exceeding
     this are physically impossible given the observed data.

Input:  (batch, WIN, 2)  — [P_agg_scaled, Q_agg_scaled] per timestep
Model:  BasicLiquidTimeLayer (fixed tau) -> LayerNorm
        -> per-appliance P heads + per-appliance Q heads
Output: P_hat (batch, n_app), Q_hat (batch, n_app)

Loss stages:
  Stage 1 (epochs 1-WARMUP_EPOCHS): MSE_P + MSE_Q only
  Stage 2 (remaining):              MSE_P + MSE_Q
                                    + lambda_P * L_phys_P
                                    + lambda_Q * L_phys_Q
                                    + lambda_nonneg * L_nonneg
                                    + lambda_bounds * L_bounds
                                    + weighted BCE (P only)

Requires pkl files generated by the updated make_whited_pkl.py
which adds columns: main_q, fridge_q, microwave_q, washing machine_q, kettle_q.
"""

