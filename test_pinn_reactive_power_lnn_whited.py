def load_data():
    print("Loading WHITED data (with reactive power columns) ...")
    splits = {}
    for name in ('train', 'val', 'test'):
        with open(f'data/WHITED_enriched/{name}.pkl', 'rb') as f:
            splits[name] = pickle.load(f)[0]
    for name, df in splits.items():
        print(f"  {name:5s}: {df.shape}  cols={list(df.columns)}")
    return splits


def create_sequences(data, window_size=WIN):
    """
    Returns:
        X:   (N, WIN, 2)  — [P_agg, Q_agg] per timestep
        Y_P: (N, n_app)   — active power at window midpoint
        Y_Q: (N, n_app)   — reactive power at window midpoint
    """
    mains_p = data['main'].values
    mains_q = data['main_q'].values
    app_p   = {app: data[app].values          for app in APPLIANCES}
    app_q   = {app: data[f'{app}_q'].values   for app in APPLIANCES}
    X, Y_P, Y_Q = [], [], []
    for i in range(0, len(mains_p) - window_size, STRIDE):
        X.append(np.stack([mains_p[i:i+window_size],
                           mains_q[i:i+window_size]], axis=1))   # (WIN, 2)
        mid = i + window_size // 2
        Y_P.append([app_p[app][mid] for app in APPLIANCES])
        Y_Q.append([app_q[app][mid] for app in APPLIANCES])
    return (np.array(X,   dtype=np.float32),
            np.array(Y_P, dtype=np.float32),
            np.array(Y_Q, dtype=np.float32))


# ---------------------------------------------------------------------------
# Per-appliance metrics
# ---------------------------------------------------------------------------

def compute_metrics(y_true_p, y_pred_p, yp_scalers):
    metrics = {}
    for i, app in enumerate(APPLIANCES):
        raw_true = yp_scalers[i].inverse_transform(y_true_p[:, i:i+1]).flatten()
        raw_pred = yp_scalers[i].inverse_transform(y_pred_p[:, i:i+1]).flatten()
        metrics[app] = calculate_nilm_metrics(raw_true, raw_pred, threshold=THRESHOLDS[app])
    return metrics


